# Uma variável pode conter letras e números, mas não pode começar com um número.
# Portanto, variavel1 e var1avel são nomes válidos, enquanto 1variavel é inválido.

# É permitido utilizar letras minúsculas e maiúsculas para nomes de variáveis.
# Entretanto, é recomendável utilizar apenas letras minúsculas.

# É importante ressaltar que Python é case sensitive,
# ou seja letras maiúsculas e minúsculas, são diferentes para o interpretador.

# Assim nome e Nome são duas variáveis diferentes,
# pois uma começa com letra minúscula e a outra começa com uma letra maiúscula.

# Não é permitido utilizar caracteres especiais (@ # $ % * ^ ' " [ ] ( ) { }),
# espaço em branco e pontuação (. ! : ? ;). Portanto, nomes como valor em R$, orçamento(%), pessoa:) são inválidos.

# Versões mais recentes do Python permitem a 
# utilização de caracteres acentuados (ex.: é ê ã à ç). Entretanto, é extremamente recomendável não utilizar estes caracteres.

# O único símbolo permitido é o caractere underscore (_) (conhecido em português por sublinhado).

# Geralmente, utilizamos este símbolo para representar espaços em branco nas variáveis
# com nomes compostos, por exemplo: nome_completo e maior_numero.

# Ele também pode ser utilizado tanto no início (_variavel), quanto no final (variavel_.)

variavel = 0
var22iavel = 99
print(variavel, var22iavel)

